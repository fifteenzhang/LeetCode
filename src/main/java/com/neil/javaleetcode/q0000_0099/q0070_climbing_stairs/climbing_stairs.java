package com.neil.javaleetcode.q0000_0099.q0070_climbing_stairs;
class climbing_stairs {
    public int climbStairs(int n) {
        int a = 0, b = 1;
        for (int i = 0; i < n; ++i) {
            int c = a + b;
            a = b;
            b = c;
        }
        return b;
    }
}
